package com.example.mobe_challenge_yaq.Bean;
/*
    Position (x,y) pour gérer la position du robot
    sur une matrice à deux dimensions
 */
public class Position {

    private int x;

    public Position(int x) {
        this.x = x;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

}
